CREATE TABLE highway_point (
	`X` FLOAT NOT NULL, 
	`Y` FLOAT NOT NULL, 
	id INTEGER NOT NULL, 
	osm_id BIGINT NOT NULL, 
	z_order INTEGER NOT NULL, 
	highway VARCHAR(15) NOT NULL, 
	name VARCHAR(34), 
	`name:en` VARCHAR(32), 
	ref INTEGER, 
	maxspeed VARCHAR(32), 
	oneway VARCHAR(32), 
	access VARCHAR(32), 
	bicycle BOOL, 
	foot VARCHAR(32), 
	horse VARCHAR(32), 
	motor_vehicle VARCHAR(32), 
	motorcar VARCHAR(32), 
	bridge VARCHAR(32), 
	cutting VARCHAR(32), 
	embankment VARCHAR(32), 
	tunnel VARCHAR(32), 
	service VARCHAR(32), 
	toll VARCHAR(32), 
	surface VARCHAR(32), 
	width VARCHAR(32), 
	construction VARCHAR(32), 
	layer VARCHAR(32), 
	junction VARCHAR(32), 
	disused VARCHAR(32), 
	CHECK (bicycle IN (0, 1))
);
