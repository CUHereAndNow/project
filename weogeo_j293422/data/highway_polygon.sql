CREATE TABLE highway_polygon (
	id INTEGER NOT NULL, 
	osm_id INTEGER NOT NULL, 
	z_order INTEGER NOT NULL, 
	highway VARCHAR(10) NOT NULL, 
	name VARCHAR(16), 
	`name:en` VARCHAR(32), 
	ref VARCHAR(32), 
	maxspeed VARCHAR(32), 
	oneway BOOL, 
	access VARCHAR(32), 
	bicycle VARCHAR(32), 
	foot BOOL, 
	horse VARCHAR(32), 
	motor_vehicle VARCHAR(32), 
	motorcar VARCHAR(32), 
	bridge VARCHAR(32), 
	cutting VARCHAR(32), 
	embankment VARCHAR(32), 
	tunnel VARCHAR(32), 
	service VARCHAR(13), 
	toll VARCHAR(32), 
	surface VARCHAR(32), 
	width VARCHAR(32), 
	construction VARCHAR(32), 
	layer VARCHAR(32), 
	junction VARCHAR(32), 
	disused VARCHAR(32), 
	CHECK (oneway IN (0, 1)), 
	CHECK (foot IN (0, 1))
);
