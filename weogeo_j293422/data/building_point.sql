CREATE TABLE building_point (
	`X` FLOAT NOT NULL, 
	`Y` FLOAT NOT NULL, 
	id INTEGER NOT NULL, 
	osm_id BIGINT NOT NULL, 
	z_order INTEGER NOT NULL, 
	building BOOL NOT NULL, 
	`building:levels` VARCHAR(32), 
	height VARCHAR(32), 
	name VARCHAR(17), 
	`name:en` VARCHAR(32), 
	craft VARCHAR(32), 
	office VARCHAR(32), 
	leisure VARCHAR(32), 
	shop VARCHAR(32), 
	`addr:housenumber` INTEGER, 
	`addr:housename` VARCHAR(32), 
	`addr:street` VARCHAR(11), 
	`addr:city` VARCHAR(7), 
	`addr:state` VARCHAR(32), 
	`addr:postcode` VARCHAR(32), 
	`addr:place` VARCHAR(32), 
	`addr:country` VARCHAR(32), 
	`addr:hamlet` VARCHAR(32), 
	`addr:suburb` VARCHAR(32), 
	`addr:subdistrict` VARCHAR(32), 
	`addr:district` VARCHAR(32), 
	`addr:province` VARCHAR(32), 
	`addr:interpolation` VARCHAR(32), 
	website VARCHAR(32), 
	`contact:website` VARCHAR(32), 
	phone VARCHAR(32), 
	`contact:phone` VARCHAR(32), 
	opening_hours VARCHAR(32), 
	brand VARCHAR(32), 
	construction VARCHAR(32), 
	`tower:type` VARCHAR(32), 
	disused VARCHAR(32), 
	CHECK (building IN (0, 1))
);
