CREATE TABLE highway_line (
	id INTEGER NOT NULL, 
	osm_id INTEGER NOT NULL, 
	z_order INTEGER NOT NULL, 
	highway VARCHAR(14) NOT NULL, 
	name VARCHAR(28), 
	`name:en` VARCHAR(32), 
	ref VARCHAR(5), 
	maxspeed VARCHAR(6), 
	oneway BOOL, 
	access VARCHAR(10), 
	bicycle VARCHAR(10), 
	foot VARCHAR(10), 
	horse BOOL, 
	motor_vehicle BOOL, 
	motorcar VARCHAR(32), 
	bridge BOOL, 
	cutting VARCHAR(32), 
	embankment VARCHAR(32), 
	tunnel VARCHAR(16), 
	service VARCHAR(13), 
	toll VARCHAR(32), 
	surface VARCHAR(15), 
	width VARCHAR(5), 
	construction VARCHAR(32), 
	layer INTEGER, 
	junction VARCHAR(32), 
	disused VARCHAR(32), 
	CHECK (oneway IN (0, 1)), 
	CHECK (horse IN (0, 1)), 
	CHECK (motor_vehicle IN (0, 1)), 
	CHECK (bridge IN (0, 1))
);
