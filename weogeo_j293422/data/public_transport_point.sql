CREATE TABLE public_transport_point (
	`X` FLOAT NOT NULL, 
	`Y` FLOAT NOT NULL, 
	id INTEGER NOT NULL, 
	osm_id BIGINT NOT NULL, 
	z_order INTEGER NOT NULL, 
	public_transport VARCHAR(8) NOT NULL, 
	name VARCHAR(34) NOT NULL, 
	`name:en` VARCHAR(32), 
	ref INTEGER NOT NULL, 
	route_ref VARCHAR(32), 
	operator VARCHAR(32), 
	network VARCHAR(32) NOT NULL, 
	train VARCHAR(32), 
	subway VARCHAR(32), 
	monorail VARCHAR(32), 
	tram VARCHAR(32), 
	bus BOOL NOT NULL, 
	trolleybus VARCHAR(32), 
	aerialway VARCHAR(32), 
	ferry VARCHAR(32), 
	shelter VARCHAR(32), 
	bench VARCHAR(32), 
	covered VARCHAR(32), 
	area VARCHAR(32), 
	CHECK (bus IN (0, 1))
);
